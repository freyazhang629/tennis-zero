// 当前语言
let currentLanguage = 'en';

// 获取浏览器语言并设置默认语言
function getDefaultLanguage() {
    const browserLang = navigator.language || navigator.userLanguage;
    const lang = browserLang.substring(0, 2);
    
    // 映射浏览器语言到支持的语言
    const langMap = {
        'en': 'en',
        'tl': 'tl',
        'ru': 'ru',
        'zh': 'zh',
        'pt': 'pt',
        'es': 'pt' // Spanish speakers might understand Portuguese better
    };
    
    return langMap[lang] || 'en';
}

// 切换语言
function switchLanguage(lang) {
    if (translations[lang]) {
        currentLanguage = lang;
        localStorage.setItem('selectedLanguage', lang);
        updatePageLanguage();
        updateLanguageSelector();
    }
}

// 更新页面语言
function updatePageLanguage() {
    const elements = document.querySelectorAll('[data-i18n]');
    elements.forEach(element => {
        const key = element.getAttribute('data-i18n');
        if (translations[currentLanguage] && translations[currentLanguage][key]) {
            if (element.tagName === 'INPUT' && element.type === 'button') {
                element.value = translations[currentLanguage][key];
            } else {
                element.textContent = translations[currentLanguage][key];
            }
        }
    });
    
    // 更新文档语言属性
    document.documentElement.lang = currentLanguage;
    
    // 更新页面标题和meta描述 (需要每个页面单独处理)
    updatePageMeta();
}

// 更新语言选择器
function updateLanguageSelector() {
    const currentLangSpan = document.querySelector('.current-lang');
    const langOptions = document.querySelectorAll('.language-option');
    
    if (currentLangSpan) {
        currentLangSpan.textContent = translations[currentLanguage]['lang-' + currentLanguage];
    }
    
    langOptions.forEach(option => {
        const lang = option.getAttribute('data-lang');
        if (lang === currentLanguage) {
            option.classList.add('active');
        } else {
            option.classList.remove('active');
        }
    });
}

// 更新页面元数据
function updatePageMeta() {
    // 这个函数需要在每个页面中具体实现
    // 因为每个页面的标题和描述都不同
}

// 初始化多语言系统
function initI18n() {
    // 获取保存的语言或使用默认语言
    const savedLanguage = localStorage.getItem('selectedLanguage');
    currentLanguage = savedLanguage || getDefaultLanguage();
    
    // 创建语言选择器
    createLanguageSelector();
    
    // 更新页面语言
    updatePageLanguage();
    
    // 更新语言选择器状态
    updateLanguageSelector();
}

// 创建语言选择器
function createLanguageSelector() {
    const headerNav = document.querySelector('.nav-list');
    if (headerNav) {
        const languageSelector = document.createElement('li');
        languageSelector.className = 'language-selector';
        languageSelector.innerHTML = `
            <div class="language-dropdown">
                <button class="language-btn" onclick="toggleLanguageDropdown()">
                    <span class="current-lang">English</span>
                    <span class="dropdown-arrow">▼</span>
                </button>
                <div class="language-options" id="language-options">
                    <div class="language-option" data-lang="en" onclick="switchLanguage('en')">
                        <span data-i18n="lang-en">English</span>
                    </div>
                    <div class="language-option" data-lang="tl" onclick="switchLanguage('tl')">
                        <span data-i18n="lang-tl">Filipino</span>
                    </div>
                    <div class="language-option" data-lang="ru" onclick="switchLanguage('ru')">
                        <span data-i18n="lang-ru">Russian</span>
                    </div>
                    <div class="language-option" data-lang="zh" onclick="switchLanguage('zh')">
                        <span data-i18n="lang-zh">Chinese</span>
                    </div>
                    <div class="language-option" data-lang="pt" onclick="switchLanguage('pt')">
                        <span data-i18n="lang-pt">Portuguese</span>
                    </div>
                </div>
            </div>
        `;
        headerNav.appendChild(languageSelector);
    }
}

// 切换语言下拉菜单
function toggleLanguageDropdown() {
    const dropdown = document.getElementById('language-options');
    dropdown.classList.toggle('show');
}

// 点击外部关闭下拉菜单
document.addEventListener('click', function(event) {
    const languageSelector = document.querySelector('.language-selector');
    if (languageSelector && !languageSelector.contains(event.target)) {
        const dropdown = document.getElementById('language-options');
        if (dropdown) {
            dropdown.classList.remove('show');
        }
    }
});

// 页面加载时初始化
document.addEventListener('DOMContentLoaded', initI18n);

// 导出函数供全局使用
window.switchLanguage = switchLanguage;
window.toggleLanguageDropdown = toggleLanguageDropdown; 